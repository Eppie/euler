bool AE32_1024( uint32_t n ) {
  if( n < 11 ) {
    return ( n == 2 || n == 3 || n == 5 || n == 7 ) ? true : false;
  }
  if( !( n % 2 ) || !( n % 3 ) || !( n % 5 ) || !( n % 7 ) ) {
    return false;
  }
  if( n < 121 ) {
    return true;
  }
  if( !( n % 11 ) || !( n % 13 ) || !( n % 17 ) || !( n % 19 ) || !( n % 23 ) || !( n % 29 ) || !( n % 31 )
      || !( n % 37 ) || !( n % 41 ) || !( n % 43 ) || !( n % 47 ) || !( n % 53 ) ) {
    return false;
  }
  if( n < 3481 ) {
    return true;
  }

  uint16_t h = AE32_1024_detail::hash( n );
  uint32_t base = AE32_1024_detail::arr[h];
  return AE32_1024_detail::prp( n, base );
}
